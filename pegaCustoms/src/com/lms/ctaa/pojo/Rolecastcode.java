package com.lms.ctaa.pojo;

public class Rolecastcode {
	private Integer id;
	private String role;
	private String castCode;
	private String castCodeName;
	private String post;

	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getCastCode() {
		return castCode;
	}
	public void setCastCode(String castCode) {
		this.castCode = castCode;
	}
	public String getCastCodeName() {
		return castCodeName;
	}
	public void setCastCodeName(String castCodeName) {
		this.castCodeName = castCodeName;
	}
	public String getPost() {
		return post;
	}
	public void setPost(String post) {
		this.post = post;
	}



}
